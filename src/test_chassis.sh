#!/bin/bash
echo "=== Terminal 0: Configure SocketCAN for ZLAC8015D ==="
echo "cd ~/ros2_ws"
echo "./scripts/setup_zlac_can.sh can0 500000"
echo "candump -tz can0"
echo ""
echo "=== Terminal 1: Start the default ZLAC chassis backend ==="
echo "cd ~/ros2_ws"
echo "source /opt/ros/humble/setup.bash"
echo "source install/setup.bash"
echo "ros2 launch ylhb_base bringup.launch.py base_backend:=zlac"
echo ""
echo "=== Terminal 2: Test moving ==="
echo "source /opt/ros/humble/setup.bash"
echo "source ~/ros2_ws/install/setup.bash"
echo "ros2 topic pub --once /cmd_vel geometry_msgs/msg/Twist \"{linear: {x: 0.1}, angular: {z: 0.0}}\""
echo "ros2 run teleop_twist_keyboard teleop_twist_keyboard"
echo ""
echo "=== Terminal 3: Check ZLAC feedback and faults ==="
echo "source /opt/ros/humble/setup.bash"
echo "source ~/ros2_ws/install/setup.bash"
echo "ros2 topic echo /odom"
echo "ros2 topic echo /zlac8015d/status"
echo "ros2 topic echo /zlac8015d/fault"
echo ""
echo "=== STM32 fallback ==="
echo "sudo chmod 777 /dev/ttyS1"
echo "ros2 launch ylhb_base bringup.launch.py base_backend:=stm32 base_port:=/dev/ttyS1"
